package com.zfoo.orm.entity.bag;

public class Item {
    private int a;
    private String b;

    public Item() {
        this.a = 1;
        this.b = "bbbb";
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    @Override
    public String toString() {
        return "Item{" +
                "a=" + a +
                ", b='" + b + '\'' +
                '}';
    }
}